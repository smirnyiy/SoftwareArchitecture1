class Location:
    def __init__(self, point):
        self.point = point

    def move(self):
        point = self.point
        return point

class Angle:
    def __init__(self, angle):
        self.angle = angle

    def rotate(self):
        angle = self.angle
        return angle


class Color:
    def __init__(self, color):
        self.color = color
        return self.color

class Power:
    def __init__(self, number):
        self.number = number
        return self.number